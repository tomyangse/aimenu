// 翻译函数（需要在组件中使用）
export function getTranslation(language: LanguageCode, key: TranslationKey, params?: Record<string, string | number>): string {
  const translation = translations[language]?.[key] || translations["zh-CN"][key] || key;
  
  if (params) {
    return Object.entries(params).reduce(
      (text, [paramKey, paramValue]) => 
        text.replace(new RegExp(`\\{${paramKey}\\}`, "g"), String(paramValue)),
      translation
    );
  }
  
  return translation;
}

// Hook for use in components
export function useTranslation() {
  // Use dynamic import to avoid circular dependency
  const { useLanguage } = require("@/contexts/language-context");
  const { language } = useLanguage();
  
  const t = (key: TranslationKey, params?: Record<string, string | number>): string => {
    return getTranslation(language, key, params);
  };
  
  return { t, language };
}

